

// 10) დაბეჭდე ყველა რიცხვი 1–დან 50-მდე, 
// მაგრამ 3-ის ჯერადი რიცხვების ნაცვლად დაბეჭდე
// "Fizz", 5-ის ჯერადი რიცხვების ნაცვლად "Buzz"
// და ორივე 3-ის და 5-ის ჯერადი რიცხვების ნაცვლად 
// "FizzBuzz" (ფიქსირებული მარტივი FizzBuzz). (ეს ცოტა რთულია)


for (let i = 0; i < 50; i++) {
    if (i % 3 === 0 && i % 5 === 0) {
        console.log("FizzBuzz")
    } else if (i % 3 === 0) {
        console.log("Fizz")
    } else if (i % 5 === 0) {
        console.log("Buzz")
    }
}