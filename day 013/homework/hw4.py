# f''-ის გამოყენებით შექმენიტ ერთი 
# დიდი ტექსტი სადაც შევა მომხმარებლის 
# მიერ შეყვანილი: 
# სახელი, გვარი, ასაკი, ქვეყანა

def fstring(firstname, lastname, age, country):

    return f"სახელი: {firstname}, გვარი: {lastname}, ასაკი: {age}, ქვეყანა: {country}."

print(fstring("ვახო", "ჯოჯუა", 15, "საქართველო"))