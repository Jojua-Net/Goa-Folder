# [1,4,3,6,9,11,17,13,26,30], თქვენი დავალებაა 
# გამოტანოთ ლუწი 
# რიცხვების ჯამი და კენტი რიცხვების რაოდენობა


mylist = [1,4,3,6,9,11,17,13,26,30]
odd = 0
even = 0
for i in mylist:
    if i % 2 != 0:
        odd += 1
    else:
        even += i

print(odd, even)